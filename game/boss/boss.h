/****************************************
 * @file   boss.h
 * @brief  ボス基底クラス
 * @author Natsume Shidara
 * @date   2026/05/15
 *
 * 各ボスは GetBossAbilities() で自身のギミック (ボス側強化版) を、
 * GetRewardAbility() で撃破時にプレイヤーが得る弱化版アビリティを返す。
 ****************************************/
#ifndef BOSS_H
#define BOSS_H

//--------------------------------------
// インクルード
//--------------------------------------
#include "ability/ability.h"

#include <memory>
#include <string>
#include <vector>

//======================================
// ボス基底クラス
//======================================
/**
 * @class  Boss
 * @brief  ステージごとの敵 (ボス) を表す抽象基底クラス
 * @detail ボス固有のギミックはアビリティとして表現される。
 *         ボス戦中はボス側強化版を、撃破後はプレイヤーへ弱化版を渡す。
 */
class Boss
{
public:
    //======================================
    // コンストラクタ／デストラクタ
    //======================================
    /** @brief 仮想デストラクタ (派生クラスを基底ポインタで破棄するため) */
    virtual ~Boss() = default;

    //--------------------------------------
    // メンバ変数 (ボス情報)
    //--------------------------------------
    std::string name;              // ボス名
    std::string description;       // ギミックの説明文
    int         difficulty = 1;    // 難易度 (★の数)

    //======================================
    // 派生クラスが実装するインターフェース
    //======================================
    /**
     * @brief  ボス側として動作するアビリティ一覧を取得
     * @return ボス戦中に登録するアビリティ群 (ボス側強化版)
     */
    virtual std::vector<std::shared_ptr<Ability>> GetBossAbilities() = 0;

    /**
     * @brief  撃破時にプレイヤーが獲得するアビリティを取得
     * @return 報酬として渡す弱化版アビリティ
     */
    virtual std::shared_ptr<Ability> GetRewardAbility() = 0;
};

#endif // BOSS_H
